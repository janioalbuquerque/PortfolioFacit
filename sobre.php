			<p>Atuar em empresas de quaisquer outros setores da economia como assessor ou recurso técnico especializado;</p>

			<p>Projetar e implementar sistemas de acordo com as necessidades da organização onde se encontra;</p>

			<p>Gerenciar infraestruturas de tecnologia da informação, elaborando políticas e diretrizes a partir da análise de necessidades;</p>

			<p>Atuar como desenvolvedor autônomo em sistemas de informação, avaliando e selecionando recursos de software e hardware;</p>

			<p>Atuar no suporte de sistemas de informação e de redes de computadores;</p>

			<p>Atuar como docente ou instrutor em escolas de ensino de informática;</p>

